public class ExerEstruturaDeRepeticao {

    public static void main(String[] args) {
        /* Faça um programa que mostre todos os números inteiros de 1 a 50. Use o laço WHILE. */
             
        int numerosInteiros = 1;
        while(numerosInteiros <= 50){
            System.out.printf("%d \n", numerosInteiros);
            numerosInteiros++;
        }
    }
}
