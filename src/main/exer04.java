public class exer4 {
    public static void main(String[] args) {
        /* Faça um programa que gere a seguinte série: 10, 20, 30, 40, ..., 990, 1000. Use o laço FOR. */
        
        int serieDez = 1000;
        for(int i = 10; i <= serieDez; i = i + 10){
            System.out.println(i);           
        }        
    }   
}
